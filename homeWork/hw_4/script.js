function getTimeFromMinutes(getMinutes) {
  let hours = Math.trunc(getMinutes / 60);
  let minutes = getMinutes % 60;
  let str = "";
  if (
    typeof getMinutes !== "number" ||
    getMinutes < 0 ||
    isNaN(getMinutes) ||
    !Number.isInteger(getMinutes)
  ) {
    return "–û—à–∏–±–∫–∞, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –¥–∞–Ω–Ω—ã–µ";
  }

  switch (hours) {
    case 0:
      str = " —á–∞—Å–æ–≤";
      break;

    case 1:
      str = " —á–∞—Å";
      break;

    case 2:
    case 3:
    case 4:
      str = " —á–∞—Å–∞";
      break;

    default:
      str = " —á–∞—Å–æ–≤";
  }
  return `—ç—Ç–æ ${hours}${str} –∏ ${minutes} –º–∏–Ω—É—Ç`;
}

